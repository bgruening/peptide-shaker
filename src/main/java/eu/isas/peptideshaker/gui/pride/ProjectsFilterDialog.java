    /**
     * Saves the filter settings and closes the dialog.
     *
     * @param evt
     */
    private void searchJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchJButtonActionPerformed

        // store the current filter values
        String[] currentFilterValues = new String[11];
        if (!accessionTextField.getText().trim().isEmpty()) {
            currentFilterValues[0] = accessionTextField.getText().trim();
        }
        if (!termTextField.getText().trim().isEmpty()) {
            currentFilterValues[1] = termTextField.getText().trim();
        }
        if (!speciesTextField.getText().trim().isEmpty()) {
            currentFilterValues[2] = speciesTextField.getText().trim();
        }
        if (!ptmsTextField.getText().trim().isEmpty()) {
            currentFilterValues[3] = ptmsTextField.getText().trim();
        }
        if (!tissueTextField.getText().trim().isEmpty()) {
            currentFilterValues[4] = tissueTextField.getText().trim();
        }
        if (!diseaseTextField.getText().trim().isEmpty()) {
            currentFilterValues[5] = diseaseTextField.getText().trim();
        }
        if (!titleTextField.getText().trim().isEmpty()) {
            currentFilterValues[6] = titleTextField.getText().trim();
        }
        if (!instrumentTextField.getText().trim().isEmpty()) {
            currentFilterValues[7] = instrumentTextField.getText().trim();
        }
        if (!experimentTextField.getText().trim().isEmpty()) {
            currentFilterValues[8] = experimentTextField.getText().trim();
        }
        if (!quantificationTextField.getText().trim().isEmpty()) {
            currentFilterValues[9] = quantificationTextField.getText().trim();
        }
        if (!tagsTextField.getText().trim().isEmpty()) {
            currentFilterValues[10] = tagsTextField.getText().trim();
        }

        // search pride
        prideReShakeGUI.setCurrentFilterValues(currentFilterValues);
    }//GEN-LAST:event_searchJButtonActionPerformed

